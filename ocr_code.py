# CELL 10 ── Full OCR Pipeline (single-image inference)
# ██████████████████████████████████████████████████████████████████████████████

class OCRPipeline:
    """
    End-to-end pipeline:
        image path → preprocess → OCR → classify → extract fields → dict
    """

    def __init__(self, classifier: DocumentClassifier):
        self.prep      = ImagePreprocessor()
        self.ocr       = OCREngine()
        self.extractor = FieldExtractor()
        self.clf       = classifier

    def process(self, image_path: str, verbose: bool = False) -> dict:
        t0 = time.time()

        # Step 1: Preprocess
        arr = self.prep.preprocess(image_path)

        # Step 2: OCR
        text, ocr_conf = self.ocr.extract_with_confidence(arr)

        # Step 3: Classify
        doc_type, clf_conf = self.clf.predict(text)

        # Step 4: Extract structured fields
        fields = self.extractor.extract(text)

        elapsed = round(time.time() - t0, 2)
        result  = {
            "file":             image_path,
            "doc_type":         doc_type,
            "clf_confidence":   clf_conf,
            "ocr_confidence":   ocr_conf,
            "processing_time_s": elapsed,
            "extracted_fields": fields,
            "raw_text_snippet": text[:400],
        }
        if verbose:
            print(json.dumps(
                {k: v for k, v in result.items() if k != "raw_text_snippet"},
                indent=2
            ))
            print(f"  text snippet: {text[:200]!r}")
        return result


