def batch_process(image_paths: list, output_csv: str = "batch_results.csv") -> pd.DataFrame:
    """
    Process a list of image paths through the full OCR pipeline.
    Returns a DataFrame and saves results to CSV.
    """
    print(f"Batch processing {len(image_paths)} images…")
    rows = []
    for i, path in enumerate(image_paths, 1):
        try:
            r = pipeline.process(str(path))
            row = {
                "file":            r["file"],
                "doc_type":        r["doc_type"],
                "clf_confidence":  r["clf_confidence"],   # ← fixed column name
                "ocr_confidence":  r["ocr_confidence"],   # ← fixed column name
                "processing_time": r["processing_time_s"],
            }
            # Flatten extracted fields as separate columns
            for field_key, field_val in r["extracted_fields"].items():
                row[f"field_{field_key}"] = field_val
            rows.append(row)
        except Exception as e:
            rows.append({
                "file":    str(path),
                "error":   str(e),
                "doc_type": "",
                "clf_confidence": 0.0,
                "ocr_confidence": 0.0,
            })

        if i % 20 == 0:
            print(f"  ✓ Processed {i}/{len(image_paths)}")

    df = pd.DataFrame(rows)
    csv_path = DIRS["outputs"] / output_csv
    df.to_csv(csv_path, index=False)
    print(f"\n✅ Batch done — {len(df)} rows saved → outputs/{output_csv}")
    return df


