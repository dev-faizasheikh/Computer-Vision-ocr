class OCREngine:
    """
    Wraps Tesseract OCR.
    - extract_text()            → cleaned string
    - extract_with_confidence() → (cleaned string, avg_confidence 0-1)
    """
    CONFIG = "--oem 3 --psm 6"   # oem3 = LSTM+Legacy; psm6 = uniform text block

    def _clean(self, text: str) -> str:
        text = re.sub(r"[^\x20-\x7E\n]", " ", text)   # keep printable ASCII
        text = re.sub(r" {2,}", " ", text)              # collapse spaces
        text = re.sub(r"\n{3,}", "\n\n", text)          # max 2 blank lines
        return text.strip()

    def extract_text(self, image_array: np.ndarray) -> str:
        pil_img = Image.fromarray(image_array)
        raw     = pytesseract.image_to_string(pil_img, config=self.CONFIG)
        return self._clean(raw)

    def extract_with_confidence(self, image_array: np.ndarray):
        pil_img = Image.fromarray(image_array)
        data    = pytesseract.image_to_data(
            pil_img, config=self.CONFIG,
            output_type=pytesseract.Output.DICT
        )
        words   = [
            (data["text"][i], int(data["conf"][i]))
            for i in range(len(data["text"]))
            if int(data["conf"][i]) > 0 and data["text"][i].strip()
        ]
        text     = " ".join(w for w, _ in words)
        avg_conf = np.mean([c for _, c in words]) / 100 if words else 0.0
        return self._clean(text), round(float(avg_conf), 4)


class FieldExtractor:
    """
    Regex-based Named Entity extraction from OCR text.
    Works on ALL Tobacco3482 document types.
    """
    PATTERNS = {
        "invoice_number": [
            r"\bINV[-\s]?(\d{3,8})\b",
            r"Invoice\s*(?:No|Number|#)[:\s]*([A-Z0-9\-]+)"
        ],
        "contract_id": [
            r"\bCTR[-\s]?(\d{3,8})\b",
            r"Contract\s*(?:ID|No|Number)[:\s]*([A-Z0-9\-]+)"
        ],
        "report_id": [
            r"\bRPT[-\s]?(\d{3,8})\b",
            r"Report\s*(?:ID|No|Number)[:\s]*([A-Z0-9\-]+)"
        ],
        "date": [
            r"\b(\d{1,2}[\/\-]\d{1,2}[\/\-]\d{2,4})\b",
            r"\b(\d{1,2}\s+(?:Jan|Feb|Mar|Apr|May|Jun|Jul|Aug|Sep|Oct|Nov|Dec)\w*\s+\d{4})\b",
            r"\b((?:Jan|Feb|Mar|Apr|May|Jun|Jul|Aug|Sep|Oct|Nov|Dec)\w*\s+\d{1,2},?\s+\d{4})\b",
        ],
        "amount": [
            r"\$\s*([\d,]+(?:\.\d{2})?)",
            r"(?:USD|EUR|GBP)\s*([\d,]+(?:\.\d{2})?)"
        ],
        "email": [
            r"[\w\.\-]+@[\w\.\-]+\.\w{2,}"
        ],
        "phone": [
            r"\(?\d{3}\)?[\s\-\.]\d{3}[\s\-\.]\d{4}"
        ],
        "percentage": [
            r"(\d{1,3}(?:\.\d+)?)\s*%"
        ],
    }

    def extract(self, text: str) -> dict:
        result = {}
        for field, patterns in self.PATTERNS.items():
            for pat in patterns:
                m = re.search(pat, text, re.IGNORECASE)
                if m:
                    result[field] = m.group(1) if m.lastindex else m.group(0)
                    break
        return result


