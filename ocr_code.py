def build_corpus(df: pd.DataFrame,
                 max_per_class: int = None,
                 cache_path: Path = None) -> pd.DataFrame:
    """
    Run OCR on every image and return a DataFrame with columns:
        path | label | ocr_text | ocr_confidence
    Uses a pickle cache so re-running is instant.
    """
    if cache_path and cache_path.exists():
        print(f"⚡ Loading cached corpus from {cache_path}")
        corpus_df = pd.read_pickle(cache_path)
        print(f"   {len(corpus_df)} entries loaded.")
        return corpus_df

    # Sample per class
    if max_per_class:
        df = (df.groupby("label", group_keys=False)
                .apply(lambda g: g.sample(min(len(g), max_per_class),
                                          random_state=42)))
        df = df.reset_index(drop=True)

    print(f"Running OCR on {len(df)} images ({df['label'].nunique()} classes)…")
    print("This may take several minutes — grab a coffee ☕\n")

    records = []
    for _, row in tqdm(df.iterrows(), total=len(df), desc="OCR"):
        try:
            arr      = preprocessor.preprocess(row["path"])
            text, cf = ocr_engine.extract_with_confidence(arr)
            records.append({
                "path":           row["path"],
                "label":          row["label"],
                "ocr_text":       text,
                "ocr_confidence": cf,
            })
        except Exception as e:
            # Skip corrupt / unreadable images silently
            records.append({
                "path":           row["path"],
                "label":          row["label"],
                "ocr_text":       "",
                "ocr_confidence": 0.0,
            })

    corpus_df = pd.DataFrame(records)

    # Drop rows where OCR returned nothing useful
    corpus_df = corpus_df[corpus_df["ocr_text"].str.strip().str.len() > 10]
    corpus_df = corpus_df.reset_index(drop=True)

    if cache_path:
        corpus_df.to_pickle(cache_path)
        print(f"\n✅ Corpus cached → {cache_path}")

    return corpus_df


