class ImagePreprocessor:
    """
    Applies a sequence of classical CV techniques to improve OCR accuracy:
      1. Convert to grayscale
      2. Fast Non-Local Means denoising
      3. Deskew (minAreaRect-based rotation correction)
      4. Adaptive Gaussian thresholding (binarization)
      5. 2× Lanczos upscaling
    """

    @staticmethod
    def load_gray(path: str) -> np.ndarray:
        """Load image and convert to grayscale."""
        img = cv2.imread(path)
        if img is None:
            img = np.array(Image.open(path).convert("L"))
            return img
        return cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)

    @staticmethod
    def denoise(gray: np.ndarray) -> np.ndarray:
        return cv2.fastNlMeansDenoising(gray, h=15, templateWindowSize=7, searchWindowSize=21)

    @staticmethod
    def deskew(gray: np.ndarray) -> np.ndarray:
        """Correct rotation up to ±45 degrees."""
        thresh = cv2.threshold(gray, 0, 255,
                               cv2.THRESH_BINARY_INV + cv2.THRESH_OTSU)[1]
        coords = np.column_stack(np.where(thresh > 0))
        if len(coords) < 10:
            return gray
        angle = cv2.minAreaRect(coords)[-1]
        angle = -(90 + angle) if angle < -45 else -angle
        if abs(angle) < 0.5:           # skip tiny corrections
            return gray
        h, w = gray.shape
        M = cv2.getRotationMatrix2D((w // 2, h // 2), angle, 1.0)
        return cv2.warpAffine(gray, M, (w, h),
                              flags=cv2.INTER_CUBIC,
                              borderMode=cv2.BORDER_REPLICATE)

    @staticmethod
    def binarize(gray: np.ndarray) -> np.ndarray:
        """Adaptive thresholding handles uneven illumination."""
        return cv2.adaptiveThreshold(
            gray, 255,
            cv2.ADAPTIVE_THRESH_GAUSSIAN_C,
            cv2.THRESH_BINARY,
            blockSize=11, C=2
        )

    @staticmethod
    def upscale(img: np.ndarray, scale: int = 2) -> np.ndarray:
        h, w = img.shape[:2]
        return cv2.resize(img, (w * scale, h * scale),
                          interpolation=cv2.INTER_LANCZOS4)

    def preprocess(self, path: str, save_path: str = None) -> np.ndarray:
        gray   = self.load_gray(path)
        gray   = self.denoise(gray)
        gray   = self.deskew(gray)
        binary = self.binarize(gray)
        result = self.upscale(binary, 2)
        if save_path:
            os.makedirs(os.path.dirname(save_path), exist_ok=True)
            cv2.imwrite(save_path, result)
        return result

    def show_stages(self, path: str):
        """Visualise each preprocessing stage side-by-side."""
        original = cv2.cvtColor(cv2.imread(path), cv2.COLOR_BGR2RGB)
        gray     = self.load_gray(path)
        denoised = self.denoise(gray.copy())
        deskewed = self.deskew(denoised.copy())
        binary   = self.binarize(deskewed.copy())
        upscaled = self.upscale(binary.copy(), 2)

        stages   = [original, gray, denoised, binary, upscaled]
        titles   = ["Original", "Grayscale", "Denoised", "Binarized", "Upscaled ×2"]
        cmaps    = [None, "gray", "gray", "gray", "gray"]

        fig, axes = plt.subplots(1, 5, figsize=(20, 4))
        for ax, img, title, cmap in zip(axes, stages, titles, cmaps):
            ax.imshow(img, cmap=cmap)
            ax.set_title(title, fontsize=11, fontweight="bold")
            ax.axis("off")
        plt.suptitle("Preprocessing Pipeline — Stage Visualisation",
                     fontsize=14, fontweight="bold")
        plt.tight_layout()
        plt.savefig(DIRS["outputs"] / "preprocessing_stages.png", dpi=150)
        plt.show()
        print("✅ Saved → outputs/preprocessing_stages.png")


