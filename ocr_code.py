def explore_dataset(dataset_root: Path, keep_classes=None):
    """
    Walk the Tobacco3482-jpg folder structure.
    Expected layout:
        Tobacco3482-jpg/
            letter/
                img001.jpg  img002.jpg …
            email/
                …
    Returns a DataFrame with columns: [path, label]
    """
    records = []
    for cls_dir in sorted(dataset_root.iterdir()):
        if not cls_dir.is_dir():
            continue
        label = cls_dir.name.lower()
        if keep_classes and label not in keep_classes:
            continue
        for img_file in cls_dir.glob("*.jpg"):
            records.append({"path": str(img_file), "label": label})
        for img_file in cls_dir.glob("*.png"):
            records.append({"path": str(img_file), "label": label})

    df = pd.DataFrame(records)
    return df

